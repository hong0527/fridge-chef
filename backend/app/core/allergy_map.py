def expand_allergies(allergies: list[str]) -> list[str]:  # NFR-EVAL-001
    """알레르기 카테고리를 세부 재료로 확장한 리스트 반환 (중복 제거·순서 보존).

    normalize_list() 보다 먼저 호출해야 한다.
    normalize_list가 먼저 실행되면 "달걀"→"계란" 변환 후
    ALLERGY_EXPANSION 키("난류")를 찾지 못해 확장이 누락된다.
    """
    seen: set[str] = set()
    out: list[str] = []
    for a in allergies:
        candidates = [a] + ALLERGY_EXPANSION.get(a, [])
        for c in candidates:
            if c not in seen:
                seen.add(c)
                out.append(c)
    return out

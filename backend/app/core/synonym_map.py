def normalize(ingredient: str) -> str:
    """단일 재료 정규화. 매핑 없으면 strip 후 원본 반환."""
    if not ingredient:
        return ingredient
    key = ingredient.strip()
    return SYNONYM_MAP.get(key, key)


def normalize_list(ingredients: list[str]) -> list[str]:
    """재료 리스트 정규화. 중복 제거(순서 보존)."""
    seen: set[str] = set()
    out: list[str] = []
    for item in ingredients:
        norm = normalize(item)
        if norm and norm not in seen:
            seen.add(norm)
            out.append(norm)
    return out

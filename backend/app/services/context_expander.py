def expand_context(user_context: str) -> str:
    """사용자 자연어 user_context → 1667 코퍼스 vocab 도메인 키워드 추가.

    매핑 키가 user_context의 부분 문자열로 등장하면 해당 키워드를 원문 뒤에
    공백으로 이어붙여 반환. 중복 키워드는 제거.

    예:
        >>> expand_context("비 오는 날 따뜻하게 먹을 국물 요리")
        '비 오는 날 따뜻하게 먹을 국물 요리 김치 두부 된장 참치김치찌개 ...'

    공백·빈 문자열은 그대로 반환 (안전 폴백).
    """
    if not user_context or not user_context.strip():
        return user_context

    expanded: list[str] = []
    seen: set[str] = set()
    for key, keywords in MOOD_MAP.items():
        if key in user_context:
            for kw in keywords:
                if kw not in seen:
                    expanded.append(kw)
                    seen.add(kw)

    if not expanded:
        return user_context
    return f"{user_context} {' '.join(expanded)}"

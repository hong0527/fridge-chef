export const ALL_INGREDIENTS = Object.entries(SYNONYM_MAP).flatMap(
  ([key, vals]) => [key, ...vals],
);

/** 로컬 자동완성: 입력 부분 일치 + 동의어 매칭 */
export function localSuggest(q: string, limit = 8): string[] {
  if (!q.trim()) return [];
  const needle = q.trim().toLowerCase();
  const hit = new Set<string>();
  for (const word of ALL_INGREDIENTS) {
    if (word.toLowerCase().includes(needle)) hit.add(word);
    if (hit.size >= limit) break;
  }
  return Array.from(hit);
}
